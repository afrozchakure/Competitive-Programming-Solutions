SinglyLinkedListNode* reverse(SinglyLinkedListNode* head) {
    SinglyLinkedListNode *curr, *prev, *next; // Creating nodes
    prev = NULL;
    next = NULL;
    curr = head;
    while(curr != NULL)
    {
       next = curr->next;  // Hint : See the pattern
       curr->next = prev;
       prev = curr;
       curr = next; 
    }
    head = prev;
    return prev;

}

